## Параллельные вычисления

Концепция параллельных вычислений не нова; она развивалась на протяжении нескольких десятилетий, начиная с первых многопроцессорных систем, которые были созданы для того, чтобы ускорить специфические научные и инженерные приложения. Развитие этой концепции было тесно связано с увеличением доступной вычислительной мощности и совершенствованием технологий.

В основе параллельных вычислений лежит использование нескольких вычислительных узлов, которые могут функционировать независимо и одновременно. В зависимости от архитектуры системы, эти процессы могут быть распределены между различными физическими или виртуальными процессорами (мультипроцессинг), ядрами в рамках одного процессора (мультикоринг) или даже различными компьютерами в сети (кластерные вычисления).

Существует разница между параллелизмом и параллельностью. В двух словах:
- Конкурентные вычисления: Прерывания
- Параллельные вычисления: Независимость

Чтобы начать думать о параллелизме, нам нужно провести различие между процессом и потоком. Процесс - это дискретный запущенный экземпляр компьютерной программы. Он имеет выделенную память для кода программы, ее данных, кучу и т. д. Каждый процесс может иметь множество вычислительных потоков. Эти потоки представляют собой единицу выполнения, которую необходимо расчитать. 

У каждой задачи есть свой стек и виртуальный процессор (виртуальный процессор, поскольку это не настоящий процессор, так как для этого нужно, чтобы задача находилась на процессоре, а этого может и не быть, потому что задача может быть временно остановлена). Затем ядро операционной системы составляет расписание задач, которое их выполняет. Для того чтобы компьютер работал без сбоев, постоянно происходит переключение контекста, то есть смена задачи, которая на самом деле запущена. Это не зависит от того, запланированы ли задачи параллельно или нет.

![](kernel-processes-and-threads-1.png)

Это важное различие, поскольку многие задачи должны выполняться параллельно, но без параллелизма. Примером может служить ввод/вывод (I/O). Операции ввода/вывода вызывают переключение привилегированного контекста, что позволяет непосредственно переключиться на задачу, которая обрабатывает ввод/вывод, чтобы продолжить действия.

Julia, как и другие языки с временем выполнения (Javascript, Go и т. д.), в своей основе представляет собой один процесс, выполняющий цикл событий. Этот цикл событий является главным потоком, а "программа Julia" или "скрипт", который вы запускаете, на самом деле выполняется в легковесном (виртуальном) потоке, который управляется главным циклом событий. 

Цикл событий переходит к поиску другой работы всякий раз, когда программа достигает точки выхода. Большее количество точек выхода позволяет более агрессивно переключать задачи, но это также означает большее количество переключений в цикле событий, что приостанавливает выполнение числовой задачи, то есть делает ее медленнее. Таким образом, выход не должен прерывать основной цикл!

## Мультипоточность

Если ваши потоки независимы, то, возможно, имеет смысл запускать их параллельно. Такая форма параллелизма известна как многопоточность.

Каждый поток имеет свой собственный стек вызовов, но именно процесс хранит кучу. Это означает, что динамически распределяемые объекты кучи делятся между потоками без каких-либо затрат - такой подход известен как вычисления с общей памятью.
